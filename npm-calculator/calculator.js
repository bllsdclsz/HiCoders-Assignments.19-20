module.exports.sum = function summation(pFirstNumber, pSecondNumber) {
    return pFirstNumber + pSecondNumber;
}

module.exports.sub = function subtraction(pFirstNumber, pSecondNumber) {
    return pFirstNumber - pSecondNumber;
}

module.exports.mul = function multiply(pFirstNumber, pSecondNumber) {
    return pFirstNumber * pSecondNumber;
}

module.exports.div = function division(pFirstNumber, pSecondNumber) {
    return pFirstNumber / pSecondNumber;
}