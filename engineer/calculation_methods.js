function circle_area (r){
    let x = 2*(Math.PI)*(Math.pow(r, 2));
    return x;
}

function rectangle_area (a, b){
    let y = 2*a*b;
    return y; 
}

function circle_circle (r) {
    let z = 2*(Math.PI)*r;
    return z;
}

function rectangle_perimeter (a, b){
    let u = 2*a*b;
    return u;
}

function cube_volume (a){
    let t = Math.pow(a, 3);
    return t;
}

function cube_area (a){
    let v = 6*(Math.pow(a, 2));
    return v;
}